import { useState, type ComponentType } from 'react';
import { Sparkles, Loader2, KeyRound, RotateCw } from 'lucide-react';

type AiResult = { text?: string; error?: string; detail?: string; usage?: { input: number; output: number } | null; cached?: boolean };

const ERROR_COPY: Record<string, string> = {
  'no-key': 'Add an API key in Settings to enable this.',
  'sdk-missing': 'AI module is unavailable in this build.',
  'invalid-key': 'API key was rejected — check it in Settings.',
  'rate-limited': 'The AI provider is busy or out of quota — try again in a moment.',
  'no-data': 'Nothing to analyze yet.',
  'no-logs': 'No log entries to analyze yet.',
  empty: 'The model returned nothing — try again.',
  failed: 'AI request failed — check your connection.',
};

// Active-provider → the brand shown in the "Generated by …" attribution line.
const PROVIDER_BRAND: Record<string, string> = { anthropic: 'Claude', openai: 'ChatGPT', google: 'Gemini' };

// Self-contained AI action card: a trigger button + loading/result/error body.
// Gated by `configured`; the model call is always explicit (button), never polled.
export default function AiPanel({
  title,
  description,
  cta,
  icon: Icon = Sparkles,
  configured,
  provider,
  run,
}: {
  title: string;
  description: string;
  cta: string;
  icon?: ComponentType<{ className?: string }>;
  configured: boolean;
  provider?: string;
  run: () => Promise<AiResult>;
}) {
  const [state, setState] = useState<{ loading: boolean; text?: string; error?: string; detail?: string; usage?: AiResult['usage']; cached?: boolean }>({ loading: false });
  const brand = (provider && PROVIDER_BRAND[provider]) || 'AI';

  const go = async () => {
    setState({ loading: true });
    try {
      const r = await run();
      if (r && r.text) setState({ loading: false, text: r.text, usage: r.usage, cached: r.cached });
      else setState({ loading: false, error: ERROR_COPY[r && r.error ? r.error : 'failed'] || 'AI request failed.', detail: (r as any)?.detail });
    } catch {
      setState({ loading: false, error: ERROR_COPY.failed });
    }
  };

  return (
    <div className="panel p-5">
      <div className="flex items-start justify-between gap-4">
        <div className="flex items-start gap-3 min-w-0">
          <div className="p-2 rounded-lg bg-primary/10 border border-primary/20 text-accent shrink-0">
            <Icon className="w-4 h-4" />
          </div>
          <div className="min-w-0">
            <h3 className="text-[13px] font-semibold text-text flex items-center gap-2">
              {title}
              <span className="text-[8px] font-mono font-bold tracking-widest text-primary bg-primary/10 border border-primary/20 px-1.5 py-0.5 rounded uppercase">AI</span>
            </h3>
            <p className="text-[11px] text-muted leading-relaxed mt-1">{description}</p>
          </div>
        </div>
        <button
          onClick={go}
          disabled={!configured || state.loading}
          title={configured ? undefined : 'Requires an AI API key (Settings)'}
          className="shrink-0 px-3.5 py-2 bg-primary/15 text-accent border border-primary/25 hover:bg-primary/25 text-xs font-medium rounded-lg transition-colors disabled:opacity-40 flex items-center gap-2"
        >
          {state.loading ? <Loader2 className="w-3.5 h-3.5 animate-spin" /> : state.text ? <RotateCw className="w-3.5 h-3.5" /> : <Sparkles className="w-3.5 h-3.5" />}
          {state.loading ? 'Thinking…' : state.text ? 'Regenerate' : cta}
        </button>
      </div>

      {!configured && (
        <div className="mt-4 flex items-center gap-2 text-[10px] font-mono text-muted bg-surface2/60 border border-border rounded-lg px-3 py-2">
          <KeyRound className="w-3.5 h-3.5 shrink-0" /> Add an AI API key in Settings to enable AI features.
        </div>
      )}

      {configured && state.loading && (
        <div className="mt-4 flex flex-col gap-2" aria-label="Generating">
          <div className="shimmer h-3 rounded w-[92%]" />
          <div className="shimmer h-3 rounded w-[78%]" />
          <div className="shimmer h-3 rounded w-[85%]" />
        </div>
      )}

      {state.error && (
        <div className="mt-4 text-[11px] font-mono text-warning bg-warning/10 border border-warning/20 rounded-lg px-3 py-2.5">
          {state.error}
          {state.detail && <div className="mt-1.5 text-[10px] text-warning/70 break-words">{state.detail}</div>}
        </div>
      )}

      {state.text && !state.loading && (
        <div className="mt-4 animate-in fade-in slide-in-from-bottom-1 duration-200">
          <div className="text-[12px] text-text/90 leading-relaxed whitespace-pre-wrap bg-background/50 border border-border rounded-lg p-4">
            {state.text}
          </div>
          <div className="mt-2 flex items-center justify-between text-[9px] font-mono text-muted/70">
            <span className="flex items-center gap-1.5"><Sparkles className="w-3 h-3 text-primary/60" /> Generated by {brand}{state.cached ? ' · cached' : ''}</span>
            {state.usage && <span>~{state.usage.input + state.usage.output} tokens</span>}
          </div>
        </div>
      )}
    </div>
  );
}
